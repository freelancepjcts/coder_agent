    print(f"🤖 ShopBot: {r['messages'][-1].content[:300]}...")

    # Turn 2: No name given — bot must remember from Turn 1
    print("\n── Turn 2 — no name in this message ────────────────────────────")
    print("👤 Priya: Can you check my order status?")
