"""---
## Part 4: The Supervisor — Intelligent Routing 🧠

### The routing problem

We have three great specialists. But how does the system know which one to call for any given customer message?

The naive approach is keyword matching:
```python
if 'order' in message or 'track' in message:
    call order_agent
elif 'recommend' in message or 'suggest' in message:
    call rec_agent
else:
    call faq_agent
```

This looks clean. But consider these real customer messages:

| Customer says | Keyword match says | Correct route |
|---|---|---|
| "My Diwali gift hasn't shown up" | ❌ FAQ (no 'order') | ✅ Order Agent |
| "Something for my friend who loves coding" | ❌ FAQ (no 'suggest') | ✅ Rec Agent |
| "Is there EMI on the phone I'm looking at?" | ❌ Ambiguous | ✅ FAQ Agent |

Keyword matching fails whenever the customer expresses intent in unexpected words.

### The solution: let the LLM read and reason

Instead of keywords, we give the routing job to the LLM itself.

The LLM reads the full message, understands its intent, and returns a routing decision:
```python
{
  "agent": "order_agent",
  "reasoning": "Customer is asking about a missing delivery"
}
```

This works on *meaning*, not words. "Diwali gift hasn't shown up" → correctly routes to Order Agent.

### What is Structured Output?

There's one challenge: if we just ask the LLM "where should this go?", it might return:
*"I think this should go to the order tracking agent, because..."*

That's a string — we'd have to parse it, and parsing can fail.

**Structured output** solves this by forcing the LLM to return a specific Python object (via Pydantic):

```python
class RouteDecision(BaseModel):
    agent: Literal["faq_agent", "order_agent", "rec_agent"]  # ONLY these values
    reasoning: str                                             # explain why
```

With `llm.with_structured_output(RouteDecision)`, the LLM is guaranteed to return
a valid `RouteDecision` object — no string parsing, no errors.

"""

# ─── ROUTING SCHEMA ───────────────────────────────────────────────────────────
class RouteDecision(BaseModel):
    agent: Literal["faq_agent", "order_agent", "rec_agent"] = Field(
        description="The specialist agent that should handle this customer query"
    )
    reasoning: str = Field(
        description="One concise sentence explaining why this agent was chosen"
    )

# The router LLM: same model but constrained to return a RouteDecision
router_llm = llm.with_structured_output(RouteDecision)

